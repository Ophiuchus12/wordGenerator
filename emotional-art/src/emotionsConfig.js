// Fonction pour trouver l'émotion la plus proche
export function findEmotion(input) {
    const normalized = input.toLowerCase().trim();

    // Recherche exacte
    if (emotionsDatabase[normalized]) {
        return emotionsDatabase[normalized];
    }

    // Recherche partielle
    for (const [key, value] of Object.entries(emotionsDatabase)) {
        if (normalized.includes(key) || key.includes(normalized)) {
            return value;
        }
    }

    // Synonymes étendus (redondants mais utiles pour les variations)
    const synonyms = {
        // Anglais
        'joy': 'joyful',
        'happiness': 'happy',
        'glad': 'happy',
        'cheerful': 'joyful',
        'sadness': 'sad',
        'sorrow': 'sad',
        'grief': 'sad',
        'blue': 'sad',
        'anger': 'angry',
        'mad': 'angry',
        'rage': 'furious',
        'peace': 'peaceful',
        'chill': 'calm',
        'energy': 'energetic',
        'pumped': 'excited',
        'fear': 'scared',
        'afraid': 'scared',
        'terror': 'frightened',
        'love': 'in love',
        'affection': 'tender',
        'sleepy': 'tired',
        'drowsy': 'sleepy',
        'shock': 'surprised',
        'trust': 'confident',
        'hope': 'hopeful',
        'concentrate': 'focused',

        // Français
        'joie': 'joyeux',
        'bonheur': 'heureux',
        'colère': 'en colère',
        'peur': 'effrayé',
        'tristesse': 'triste',
        'sérénité': 'serein',
        'paix': 'paisible',
        'énergie': 'énergique',
        'angoisse': 'anxieux',
        'repos': 'détendu',
        'passion': 'passionné',
        'espoir': 'plein d\'espoir',
        'rêve': 'rêveur',
        'focus': 'concentré',
        'méditation': 'méditatif',
        'courage': 'courageux',
        'force': 'fort'
    };

    for (const [synonym, emotion] of Object.entries(synonyms)) {
        if (normalized.includes(synonym)) {
            return emotionsDatabase[emotion] || emotionsDatabase['neutral'];
        }
    }

    return emotionsDatabase['neutral'];
}